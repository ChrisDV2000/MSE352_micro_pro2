ORG 0H

;8051 SETUP INSTRUCTIONS
;INPUTS
	MOV P0, #0FFH
	MOV P2, #0FFH
;OUTPUTS
	MOV P1, #0H
	MOV P3, #0H

;PROGRAM

START:
	CLR 0.7 ; TURN 7 SEGMENT DISPLAYS OFF
	;LCD P1, P1.2 = ENABLE, P1.3 = REGISTER SELECT
	;LCD IS A HD44780
	;MODE SELECT
	
	CLR P1.3
	CLR P1.2
	SETB P1.2
	MOV P1, #01H
	CLR P1.2
	SETB P1.2

	JMP START
END